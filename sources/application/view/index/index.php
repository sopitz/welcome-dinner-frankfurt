        <p>Was ist Welcome Dinner?</p>
    </div>
    <div class="clearfix colelem" id="u2384-17"><!-- content -->
        <p>Frankfurt am Main – das ist die Stadt des Geldes, der regen Betriebsamkeit. Aber gleichwohl auch Stadt der Gemütlichkeit, Stadt der Gemeinschaft und Stadt der Offenheit. Frankfurt bietet auf engstem Raum eine soziale Vielfalt, die ihresgleichen sucht. Und ihre Bewohner schlagen seit jeher Brücken zwischen unterschiedlichsten Kulturen. Nun, wo eine Vielzahl von Menschen diese Stadt erreicht, in der sie sich – auf der Suche nach Schutz und Zuflucht – ein besseres, sicheres Leben erhoffen, gilt es erneut Brücken zu schlagen.</p>
        <p>&nbsp;</p>
        <p>Wie ginge das einfacher als anlässlich eines gemeinsamen <span id="u2384-5">Dinners?</span></p>
        <p>&nbsp;</p>
        <p>Ein gemeinsames Essen ist immer noch die beste Möglichkeit, um sich kennenzulernen.</p>
        <p>Alteingesessene treffen auf Neuankömmlinge, Alt auf Jung. Gemeinsam muss dabei nicht einmal die Sprache sein – einzig das Interesse am Kulturaustausch sowie die Bereitschaft, einen Grundstein für ein segensreiches künftiges Miteinander zu legen, ist Voraussetzung.</p>
        <p>&nbsp;</p>
        <p>Gemeinsam lachen, gemeinsam genießen, Gemeinschaft leben, Trennung überwinden, Sprache lernen, Beziehung starten, Willkommen heißen, Integration leben - das wollen wir für und in Frankfurt!</p>
